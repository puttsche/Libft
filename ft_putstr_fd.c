#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int		x;

	if (s != NULL)
	{
		x = 0;
		while (s[x])
		{
			ft_putchar_fd(s[x], fd);
			x++;
		}
	}
}

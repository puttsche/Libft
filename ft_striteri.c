#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				x;
	unsigned int	y;

	if (s != NULL && (*f))
	{
		x = 0;
		y = 0;
		while (*s)
		{
			(*f)(y, s);
			s++;
			y++;
		}
	}
}

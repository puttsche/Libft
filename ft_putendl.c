#include "libft.h"

void	ft_putendl(char const *s)
{
	int		x;

	if (s != NULL)
	{
		x = 0;
		while (s[x] != '\0')
		{
			ft_putchar(s[x]);
			x++;
		}
		if (s[x] == '\0')
			ft_putchar('\n');
	}
}
